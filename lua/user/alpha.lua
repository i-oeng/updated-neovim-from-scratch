dashboard.section.buttons.val = function()
	local buttons = vim.o.columns < 42 and compact_buttons or full_buttons
	local width = math.max(12, math.min(42, vim.o.columns - 4))
	for _, button in ipairs(buttons) do
		button.opts.width = width
		button.opts.cursor = math.min(3, math.max(width - 2, 0))
	end
	return buttons
end
dashboard.section.buttons.opts.spacing = 1

