dashboard.section.header.val = function()
	local max_width = 0
	for _, line in ipairs(full_header) do
		max_width = math.max(max_width, vim.fn.strdisplaywidth(line))
	end

	local art_fits_width = max_width <= math.max(vim.o.columns - 4, 1)
	local art_fits_height = (#full_header + 11) <= vim.o.lines
	if art_fits_width and art_fits_height then
		return full_header
	end
	return { "NVIM" }
end

